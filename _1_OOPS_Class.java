package session1;

public class _1_OOPS_Class {
    int i = 10;
    String s = "FirstName";

    // Select menu Code >Generate > Constructor
    // int body type sout and please click enter
    public _1_OOPS_Class() {
        System.out.println("statement 1");
        System.out.println("statement 2");
        System.out.println("statement 3");
        i = 0;
        s = "New name";
    }


    ///  Select menu Code >Generate >  Getter and setter > select both variables using Control keys
    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}


